#include "push_swap.h"

long int	ft_atoi(const char *str)
{
	long int	i;
	long int	pos;
	long int	ris;

	i = 0;
	ris = 0;
	pos = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			pos = pos * -1;
		i++;
	}
	if (str[i] == '-' || str[i] == '+')
		return (0);
	while (str[i] >= '0' && str[i] <= '9')
	{
		ris = (ris * 10) + (str[i] - '0');
		i++;
	}
	return (ris * pos);
}
